<script>
    // Emotional animation effects
    function initEmotionalAnimations() {
        const hearts = document.querySelectorAll('.heart-pulse');
        hearts.forEach((heart, index) => {
            setTimeout(() => {
                heart.style.animation = 'heartbeat 3s ease-in-out infinite';
            }, index * 300);
        });

        const comfort = document.querySelectorAll('.comfort-rotate');
        comfort.forEach(el => {
            el.style.animation = 'gentle-comfort 6s linear infinite';
        });
    }

    // Gentle sway animation
    function initGentleAnimations() {
        const heroElements = document.querySelectorAll('.hero-animation');
        heroElements.forEach(el => {
            el.style.animation = 'gentle-sway 8s ease-in-out infinite';
        });
    }

    // Smooth scrolling for back to top
    function scrollToTop() {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }

    // Mature card hover effects
    function initMatureEffects() {
        const cards = document.querySelectorAll('.mature-card');
        cards.forEach(card => {
            card.addEventListener('mouseenter', () => {
                card.style.transform = 'translateY(-8px)';
                card.style.boxShadow = '0 20px 40px rgba(190, 24, 93, 0.3)';
            });
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'translateY(0)';
                card.style.boxShadow = '';
            });
        });
    }

    // Understanding wave animation
    function initUnderstandingWaves() {
        const waves = document.querySelectorAll('.understanding-wave');
        waves.forEach(wave => {
            wave.style.animation = 'understanding-flow 4s ease-in-out infinite';
        });
    }

    // Initialize all mature effects
    document.addEventListener('DOMContentLoaded', function() {
        initEmotionalAnimations();
        initGentleAnimations();
        initMatureEffects();
        initUnderstandingWaves();

        // Show/hide back to top button
        window.addEventListener('scroll', function() {
            const button = document.querySelector('button[onclick="scrollToTop()"]');
            if (window.pageYOffset > 300) {
                button.style.display = 'block';
            } else {
                button.style.display = 'none';
            }
        });

        // Performance tracking
        if (typeof gtag !== 'undefined') {
            gtag('event', 'page_view', {
                page_title: 'Unsatisfied Bhabhi Mumbai',
                page_location: window.location.href,
                custom_parameter: 'mature_companions'
            });
        }
    });

    // Mature companion booking tracking
    function trackMatureBooking(method, companionType) {
        if (typeof gtag !== 'undefined') {
            gtag('event', 'mature_booking_click', {
                booking_method: method,
                companion_type: companionType || 'general',
                service_type: 'unsatisfied_bhabhi'
            });
        }
    }

    // Add tracking to booking buttons
    document.addEventListener('DOMContentLoaded', function() {
        const phoneButtons = document.querySelectorAll('a[href^="tel:"]');
        phoneButtons.forEach(button => {
            button.addEventListener('click', () => trackMatureBooking('phone'));
        });

        const whatsappButtons = document.querySelectorAll('a[href*="whatsapp"]');
        whatsappButtons.forEach(button => {
            button.addEventListener('click', () => trackMatureBooking('whatsapp'));
        });

        // Form submission tracking
        const forms = document.querySelectorAll('form');
        forms.forEach(form => {
            form.addEventListener('submit', (e) => {
                e.preventDefault();
                const companionType = form.querySelector('select').value;
                trackMatureBooking('form', companionType);
                // Handle form submission here
            });
        });
    });
