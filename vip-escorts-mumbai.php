<script>
    // VIP luxury animation effects
    function initVIPAnimations() {
        const crowns = document.querySelectorAll('.crown-spin');
        crowns.forEach((crown, index) => {
            setTimeout(() => {
                crown.style.animation = 'royal-crown 6s ease-in-out infinite';
            }, index * 500);
        });

        const exclusive = document.querySelectorAll('.exclusive-dance');
        exclusive.forEach(el => {
            el.style.animation = 'exclusive-movement 12s ease-in-out infinite';
        });

        const elite = document.querySelectorAll('.elite-rotate');
        elite.forEach(el => {
            el.style.animation = 'elite-luxury 10s linear infinite';
        });
    }

    // Luxury floating animation
    function initLuxuryAnimations() {
        const heroElements = document.querySelectorAll('.hero-animation');
        heroElements.forEach(el => {
            el.style.animation = 'luxury-float 8s ease-in-out infinite';
        });
    }

    // Smooth scrolling for back to top
    function scrollToTop() {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }

    // Luxury card hover effects
    function initLuxuryEffects() {
        const cards = document.querySelectorAll('.luxury-card');
        cards.forEach(card => {
            card.addEventListener('mouseenter', () => {
                card.style.transform = 'translateY(-15px) scale(1.05)';
                card.style.boxShadow = '0 30px 60px rgba(251, 191, 36, 0.4)';
                card.style.borderColor = '#f59e0b';
            });
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'translateY(0) scale(1)';
                card.style.boxShadow = '';
                card.style.borderColor = '#fbbf24';
            });
        });
    }

    // Diamond wave animation
    function initDiamondWaves() {
        const waves = document.querySelectorAll('.diamond-wave');
        waves.forEach(wave => {
            wave.style.animation = 'diamond-flow 6s ease-in-out infinite';
        });
    }

    // VIP pulse effects
    function initVIPPulse() {
        const pulses = document.querySelectorAll('.vip-pulse');
        pulses.forEach((pulse, index) => {
            setTimeout(() => {
                pulse.style.animation = 'vip-heartbeat 4s ease-in-out infinite';
            }, index * 800);
        });
    }

    // Initialize all VIP effects
    document.addEventListener('DOMContentLoaded', function() {
        initVIPAnimations();
        initLuxuryAnimations();
        initLuxuryEffects();
        initDiamondWaves();
        initVIPPulse();

        // Show/hide back to top button
        window.addEventListener('scroll', function() {
            const button = document.querySelector('button[onclick="scrollToTop()"]');
            if (window.pageYOffset > 300) {
                button.style.display = 'block';
            } else {
                button.style.display = 'none';
            }
        });

        // Performance tracking
        if (typeof gtag !== 'undefined') {
            gtag('event', 'page_view', {
                page_title: 'VIP Escorts Mumbai',
                page_location: window.location.href,
                custom_parameter: 'vip_luxury'
            });
        }
    });

    // VIP service tracking
    function trackVIPService(method, serviceType) {
        if (typeof gtag !== 'undefined') {
            gtag('event', 'vip_service_click', {
                contact_method: method,
                service_type: serviceType || 'general',
                vip_tier: 'premium'
            });
        }
    }

    // Add tracking to contact buttons
    document.addEventListener('DOMContentLoaded', function() {
        const phoneButtons = document.querySelectorAll('a[href^="tel:"]');
        phoneButtons.forEach(button => {
            button.addEventListener('click', () => trackVIPService('phone'));
        });

        const whatsappButtons = document.querySelectorAll('a[href*="whatsapp"]');
        whatsappButtons.forEach(button => {
            button.addEventListener('click', () => trackVIPService('whatsapp'));
        });

        // Form submission tracking
        const forms = document.querySelectorAll('form');
        forms.forEach(form => {
            form.addEventListener('submit', (e) => {
                e.preventDefault();
                const serviceType = form.querySelector('select').value;
                trackVIPService('form', serviceType);
                // Handle form submission here
            });
        });
    });
