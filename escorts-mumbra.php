        const heroElements = document.querySelectorAll('.hero-animation');
        heroElements.forEach(el => {
            el.style.animation = 'mumbra-float 7s ease-in-out infinite';
        });
    }

    // Navi card hover effects
    function initNaviEffects() {
        const cards = document.querySelectorAll('.navi-card');
        cards.forEach(card => {
            card.addEventListener('mouseenter', () => {
                card.style.transform = 'translateY(-12px) scale(1.05)';
                card.style.boxShadow = '0 25px 50px rgba(236, 72, 153, 0.3)';
                card.style.borderColor = '#db2777';
            });
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'translateY(0) scale(1)';
                card.style.boxShadow = '';
                card.style.borderColor = '#ec4899';
            });
        });
    }

    // Luxury wave animation
    function initLuxuryWaves() {
        const waves = document.querySelectorAll('.luxury-wave');
        waves.forEach(wave => {
            wave.style.animation = 'luxury-flow 5s ease-in-out infinite';
        });
    }

    // Luxury pulse effects
    function initLuxuryPulse() {
        const pulses = document.querySelectorAll('.luxury-pulse');
        pulses.forEach((pulse, index) => {
            setTimeout(() => {
                pulse.style.animation = 'luxury-heartbeat 4s ease-in-out infinite';
            }, index * 700);
        });
    }

    // Initialize all mumbra effects
    document.addEventListener('DOMContentLoaded', function() {
        initMumbraAnimations();
        initLuxuryAnimations();
        initNaviEffects();
        initLuxuryWaves();
        initLuxuryPulse();
        initSEOOptimizations(); // Add SEO optimizations

        // Performance tracking
        if (typeof gtag !== 'undefined') {
            gtag('event', 'page_view', {
                page_title: 'Premium Escorts in Mumbra',
                page_location: window.location.href,
                custom_parameter: 'mumbra_escorts'
            });
        }
    });

    // Mumbra service tracking
    function trackMumbraClick(method, location) {
        if (typeof gtag !== 'undefined') {
            gtag('event', 'mumbra_service_click', {
                contact_method: method,
                location: location || 'mumbra',
                service_type: 'navi_mumbai_escorts'
            });
        }
    }

    // Add tracking to contact buttons
    document.addEventListener('DOMContentLoaded', function() {
        const phoneButtons = document.querySelectorAll('a[href^="tel:"]');
        phoneButtons.forEach(button => {
            button.addEventListener('click', () => trackMumbraClick('phone', 'mumbra'));
        });

        const whatsappButtons = document.querySelectorAll('a[href*="whatsapp"]');
        whatsappButtons.forEach(button => {
            button.addEventListener('click', () => trackMumbraClick('whatsapp', 'mumbra'));
        });

        // Form submission tracking
        const forms = document.querySelectorAll('form');
        forms.forEach(form => {
            form.addEventListener('submit', (e) => {
                e.preventDefault();
                const location = form.querySelector('select').value;
                trackMumbraClick('form', 'mumbra_' + location);
                // Handle form submission here
            });
        });
    });
