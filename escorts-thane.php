<script>
    // Premium thane animation effects with performance optimization
    function initThaneAnimations() {
        // Use requestAnimationFrame for better performance
        requestAnimationFrame(() => {
            const locations = document.querySelectorAll('.navi-spin');
            locations.forEach((location, index) => {
                setTimeout(() => {
                    location.style.animation = 'navi-rotate 8s ease-in-out infinite';
                }, index * 400);
            });

            const services = document.querySelectorAll('.thane-dance');
            services.forEach(service => {
                service.style.animation = 'thane-movement 11s ease-in-out infinite';
            });

            const premium = document.querySelectorAll('.premium-rotate');
            premium.forEach(el => {
                el.style.animation = 'premium-spin 10s linear infinite';
            });
        });
    }
    
    // SEO & Performance optimizations
    function initSEOOptimizations() {
        // Add structured data for dynamic content
        const mainHeading = document.querySelector('h1');
        if (mainHeading) {
            mainHeading.setAttribute('itemscope', '');
            mainHeading.setAttribute('itemtype', 'https://schema.org/Headline');
        }
        
        // Lazy load images for better performance
        const images = document.querySelectorAll('img[data-src]');
        const imageObserver = new IntersectionObserver((entries, observer) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const img = entry.target;
                    img.src = img.dataset.src;
                    img.classList.remove('lazy');
                    observer.unobserve(img);
                }
            });
        });
        
        images.forEach(img => imageObserver.observe(img));
        
        // Track "Escorts In Thane" keyword interactions
        document.addEventListener('click', function(e) {
            if (e.target.tagName === 'A' || e.target.closest('a')) {
                // Enhanced user experience tracking
                gtag('event', 'click', {
                    'event_category': 'Escorts In Thane',
                    'event_label': 'User Interaction'
                });
            }
        });
    }

    // Thane floating animation
    function initLuxuryAnimations() {
        const heroElements = document.querySelectorAll('.hero-animation');
        heroElements.forEach(el => {
            el.style.animation = 'thane-float 7s ease-in-out infinite';
        });
    }

    // Navi card hover effects
    function initNaviEffects() {
        const cards = document.querySelectorAll('.navi-card');
        cards.forEach(card => {
            card.addEventListener('mouseenter', () => {
                card.style.transform = 'translateY(-12px) scale(1.05)';
                card.style.boxShadow = '0 25px 50px rgba(236, 72, 153, 0.3)';
                card.style.borderColor = '#db2777';
            });
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'translateY(0) scale(1)';
                card.style.boxShadow = '';
                card.style.borderColor = '#ec4899';
            });
        });
    }

    // Luxury wave animation
    function initLuxuryWaves() {
        const waves = document.querySelectorAll('.luxury-wave');
        waves.forEach(wave => {
            wave.style.animation = 'luxury-flow 5s ease-in-out infinite';
        });
    }

    // Luxury pulse effects
    function initLuxuryPulse() {
        const pulses = document.querySelectorAll('.luxury-pulse');
        pulses.forEach((pulse, index) => {
            setTimeout(() => {
                pulse.style.animation = 'luxury-heartbeat 4s ease-in-out infinite';
            }, index * 700);
        });
    }

