<script>
    // Supportive animation effects
    function initSupportAnimations() {
        const flowers = document.querySelectorAll('.flower-bloom');
        flowers.forEach((flower, index) => {
            setTimeout(() => {
                flower.style.animation = 'bloom-pulse 4s ease-in-out infinite';
            }, index * 400);
        });

        const butterflies = document.querySelectorAll('.butterfly-dance');
        butterflies.forEach(butterfly => {
            butterfly.style.animation = 'butterfly-flutter 10s ease-in-out infinite';
        });

        const caring = document.querySelectorAll('.caring-rotate');
        caring.forEach(el => {
            el.style.animation = 'gentle-care 8s linear infinite';
        });
    }

    // Blossom floating animation
    function initBlossomAnimations() {
        const heroElements = document.querySelectorAll('.hero-animation');
        heroElements.forEach(el => {
            el.style.animation = 'blossom-float 7s ease-in-out infinite';
        });
    }

    // Smooth scrolling for back to top
    function scrollToTop() {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }

    // Support card hover effects
    function initSupportEffects() {
        const cards = document.querySelectorAll('.support-card');
        cards.forEach(card => {
            card.addEventListener('mouseenter', () => {
                card.style.transform = 'translateY(-10px)';
                card.style.boxShadow = '0 25px 50px rgba(249, 115, 22, 0.3)';
            });
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'translateY(0)';
                card.style.boxShadow = '';
            });
        });
    }

    // Empowerment wave animation
    function initEmpowermentWaves() {
        const waves = document.querySelectorAll('.empowerment-wave');
        waves.forEach(wave => {
            wave.style.animation = 'empowerment-flow 5s ease-in-out infinite';
        });
    }

    // Initialize all support effects
    document.addEventListener('DOMContentLoaded', function() {
        initSupportAnimations();
        initBlossomAnimations();
        initSupportEffects();
        initEmpowermentWaves();

        // Show/hide back to top button
        window.addEventListener('scroll', function() {
            const button = document.querySelector('button[onclick="scrollToTop()"]');
            if (window.pageYOffset > 300) {
                button.style.display = 'block';
            } else {
                button.style.display = 'none';
            }
        });

        // Performance tracking
        if (typeof gtag !== 'undefined') {
            gtag('event', 'page_view', {
                page_title: 'Unsatisfied Female Mumbai',
                page_location: window.location.href,
                custom_parameter: 'women_support'
            });
        }
    });

    // Women support tracking
    function trackWomenSupport(method, supportType) {
        if (typeof gtag !== 'undefined') {
            gtag('event', 'women_support_click', {
                contact_method: method,
                support_type: supportType || 'general',
                service_type: 'unsatisfied_female'
            });
        }
    }

    // Add tracking to contact buttons
    document.addEventListener('DOMContentLoaded', function() {
        const phoneButtons = document.querySelectorAll('a[href^="tel:"]');
        phoneButtons.forEach(button => {
            button.addEventListener('click', () => trackWomenSupport('phone'));
        });

        const whatsappButtons = document.querySelectorAll('a[href*="whatsapp"]');
        whatsappButtons.forEach(button => {
            button.addEventListener('click', () => trackWomenSupport('whatsapp'));
        });

        // Form submission tracking
        const forms = document.querySelectorAll('form');
        forms.forEach(form => {
            form.addEventListener('submit', (e) => {
                e.preventDefault();
                const supportType = form.querySelector('select').value;
                trackWomenSupport('form', supportType);
                // Handle form submission here
            });
        });
    });
</script>

