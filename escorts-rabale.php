    function trackRabaleClick(method, location) {
        if (typeof gtag !== 'undefined') {
            gtag('event', 'rabale_service_click', {
                contact_method: method,
                location: location || 'rabale',
                service_type: 'navi_mumbai_escorts'
            });
        }
    }

