<script>
    // Elite sparkle effect
    function createSparkle() {
        const sparkles = document.querySelectorAll('.diamond-sparkle');
        sparkles.forEach((sparkle, index) => {
            setTimeout(() => {
                sparkle.style.animation = 'sparkle 3s ease-in-out infinite';
            }, index * 200);
        });
    }

    // Luxury floating animation
    function initLuxuryAnimations() {
        const heroElements = document.querySelectorAll('.hero-animation');
        heroElements.forEach(el => {
            el.style.animation = 'luxury-float 6s ease-in-out infinite';
        });
    }

    // Smooth scrolling for back to top
    function scrollToTop() {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }

    // Elite hover effects
    function initEliteEffects() {
        const cards = document.querySelectorAll('.luxury-card');
        cards.forEach(card => {
            card.addEventListener('mouseenter', () => {
                card.style.transform = 'translateY(-8px)';
                card.style.boxShadow = '0 20px 40px rgba(255, 215, 0, 0.3)';
            });
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'translateY(0)';
                card.style.boxShadow = '';
            });
        });
    }

    // Crown glow effect
    function initCrownGlow() {
        const crowns = document.querySelectorAll('.crown-glow');
        crowns.forEach(crown => {
            crown.style.animation = 'crown-shine 4s ease-in-out infinite';
        });
    }

    // Initialize all luxury effects
    document.addEventListener('DOMContentLoaded', function() {
        createSparkle();
        initLuxuryAnimations();
        initEliteEffects();
        initCrownGlow();

        // Show/hide back to top button
        window.addEventListener('scroll', function() {
            const button = document.querySelector('button[onclick="scrollToTop()"]');
            if (window.pageYOffset > 300) {
                button.style.display = 'block';
            } else {
                button.style.display = 'none';
            }
        });

        // Performance tracking
        if (typeof gtag !== 'undefined') {
            gtag('event', 'page_view', {
                page_title: 'Top Class Model Mumbai',
                page_location: window.location.href,
                custom_parameter: 'elite_models'
            });
        }
    });

    // Elite booking tracking
    function trackEliteBooking(method) {
        if (typeof gtag !== 'undefined') {
            gtag('event', 'elite_booking_click', {
                booking_method: method,
                service_type: 'top_class_model'
            });
        }
    }

    // Add tracking to booking buttons
    document.addEventListener('DOMContentLoaded', function() {
        const phoneButtons = document.querySelectorAll('a[href^="tel:"]');
        phoneButtons.forEach(button => {
            button.addEventListener('click', () => trackEliteBooking('phone'));
        });

        const whatsappButtons = document.querySelectorAll('a[href*="whatsapp"]');
        whatsappButtons.forEach(button => {
            button.addEventListener('click', () => trackEliteBooking('whatsapp'));
        });
    });
</script>

