<!-- JavaScript -->
<script>
    // Smooth scrolling for back to top
    function scrollToTop() {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }

    // Show/hide back to top button
    window.addEventListener('scroll', function() {
        const button = document.querySelector('button[onclick="scrollToTop()"]');
        if (window.pageYOffset > 300) {
            button.style.display = 'block';
        } else {
            button.style.display = 'none';
        }
    });

    // Initialize page
    document.addEventListener('DOMContentLoaded', function() {
        // Add animation classes
        const elements = document.querySelectorAll('.hover-scale');
        elements.forEach(el => {
            el.addEventListener('mouseenter', () => {
                el.style.transform = 'scale(1.05)';
            });
            el.addEventListener('mouseleave', () => {
                el.style.transform = 'scale(1)';
            });
        });

        // Privacy section animations
        const privacySections = document.querySelectorAll('.privacy-section');
        privacySections.forEach((section, index) => {
            section.style.animationDelay = `${index * 0.1}s`;
        });

        // Performance tracking
        if (typeof gtag !== 'undefined') {
            gtag('event', 'page_view', {
                page_title: 'Privacy Policy',
                page_location: window.location.href
            });
        }
    });

    // Scroll animations
    window.addEventListener('scroll', function() {
        const sections = document.querySelectorAll('.privacy-section');
        sections.forEach(section => {
            const rect = section.getBoundingClientRect();
            if (rect.top < window.innerHeight && rect.bottom > 0) {
                section.style.opacity = '1';
                section.style.transform = 'translateY(0)';
            }
        });
    });
</script>

