<script>
    // Travel animation effects
    function initTravelAnimations() {
        const planes = document.querySelectorAll('.plane-animation');
        planes.forEach((plane, index) => {
            setTimeout(() => {
                plane.style.animation = 'plane-fly 6s ease-in-out infinite';
            }, index * 500);
        });

        const globes = document.querySelectorAll('.globe-spin');
        globes.forEach(globe => {
            globe.style.animation = 'globe-rotate 10s linear infinite';
        });

        const compass = document.querySelectorAll('.compass-rotate');
        compass.forEach(comp => {
            comp.style.animation = 'compass-spin 8s linear infinite';
        });
    }

    // Journey floating animation
    function initJourneyFloat() {
        const heroElements = document.querySelectorAll('.hero-animation');
        heroElements.forEach(el => {
            el.style.animation = 'journey-float 5s ease-in-out infinite';
        });
    }

    // Smooth scrolling for back to top
    function scrollToTop() {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    }

    // Travel card hover effects
    function initTravelEffects() {
        const cards = document.querySelectorAll('.travel-card');
        cards.forEach(card => {
            card.addEventListener('mouseenter', () => {
                card.style.transform = 'translateY(-8px)';
                card.style.boxShadow = '0 20px 40px rgba(8, 145, 178, 0.3)';
            });
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'translateY(0)';
                card.style.boxShadow = '';
            });
        });
    }

    // Wave animation for gradient elements
    function initWaveAnimations() {
        const waves = document.querySelectorAll('.wave-animation');
        waves.forEach(wave => {
            wave.style.animation = 'wave-move 3s ease-in-out infinite';
        });
    }

    // Initialize all travel effects
    document.addEventListener('DOMContentLoaded', function() {
        initTravelAnimations();
        initJourneyFloat();
        initTravelEffects();
        initWaveAnimations();

        // Show/hide back to top button
        window.addEventListener('scroll', function() {
            const button = document.querySelector('button[onclick="scrollToTop()"]');
            if (window.pageYOffset > 300) {
                button.style.display = 'block';
            } else {
                button.style.display = 'none';
            }
        });

        // Performance tracking
        if (typeof gtag !== 'undefined') {
            gtag('event', 'page_view', {
                page_title: 'Travel Escorts Mumbai',
                page_location: window.location.href,
                custom_parameter: 'travel_companions'
            });
        }
    });

    // Travel booking tracking
    function trackTravelBooking(method, travelType) {
        if (typeof gtag !== 'undefined') {
            gtag('event', 'travel_booking_click', {
                booking_method: method,
                travel_type: travelType || 'general',
                service_type: 'travel_escorts'
            });
        }
    }

    // Add tracking to booking buttons
    document.addEventListener('DOMContentLoaded', function() {
        const phoneButtons = document.querySelectorAll('a[href^="tel:"]');
        phoneButtons.forEach(button => {
            button.addEventListener('click', () => trackTravelBooking('phone'));
        });

        const whatsappButtons = document.querySelectorAll('a[href*="whatsapp"]');
        whatsappButtons.forEach(button => {
            button.addEventListener('click', () => trackTravelBooking('whatsapp'));
        });

        // Form submission tracking
        const forms = document.querySelectorAll('form');
        forms.forEach(form => {
            form.addEventListener('submit', (e) => {
                e.preventDefault();
                const travelType = form.querySelector('select').value;
                trackTravelBooking('form', travelType);
                // Handle form submission here
            });
        });
    });
</script>

