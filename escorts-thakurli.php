    // Premium thakurli animation effects with performance optimization
    function initThakurliAnimations() {
        // Use requestAnimationFrame for better performance
        requestAnimationFrame(() => {
            const locations = document.querySelectorAll('.navi-spin');
            locations.forEach((location, index) => {
                setTimeout(() => {
                    location.style.animation = 'navi-rotate 8s ease-in-out infinite';
                }, index * 400);
            });

            const services = document.querySelectorAll('.thakurli-dance');
            services.forEach(service => {
                service.style.animation = 'thakurli-movement 11s ease-in-out infinite';
            });

            const premium = document.querySelectorAll('.premium-rotate');
            premium.forEach(el => {
                el.style.animation = 'premium-spin 10s linear infinite';
            });
        });
    }
    
    // SEO & Performance optimizations
    function initSEOOptimizations() {
        // Add structured data for dynamic content
        const mainHeading = document.querySelector('h1');
        if (mainHeading) {
            mainHeading.setAttribute('itemscope', '');
            mainHeading.setAttribute('itemtype', 'https://schema.org/Headline');
        }
        
        // Lazy load images for better performance
        const images = document.querySelectorAll('img[data-src]');
        const imageObserver = new IntersectionObserver((entries, observer) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const img = entry.target;
                    img.src = img.dataset.src;
                    img.classList.remove('lazy');
                    observer.unobserve(img);
                }
            });
        });
        
        images.forEach(img => imageObserver.observe(img));
        
        // Track "Escorts In Thakurli" keyword interactions
        document.addEventListener('click', function(e) {
            if (e.target.tagName === 'A' || e.target.closest('a')) {
                // Enhanced user experience tracking
                gtag('event', 'click', {
                    'event_category': 'Escorts In Thakurli',
                    'event_label': 'User Interaction'
                });
            }
        });
    }

    // Thakurli floating animation
    function initLuxuryAnimations() {
        const heroElements = document.querySelectorAll('.hero-animation');
